package Alphabet;

public class atoz {
    static void main(String[] args) {
        for (char ch = 'a'; ch <= 'z'; ch++) {
            System.out.println(ch);
        }
        for (char ch = 'A'; ch <= 'Z'; ch++) {
            System.out.println(ch);
        }
        for (int ch = 'a'; ch <= 'z'; ch++) {
            System.out.println(ch);
        }
        for (int ch = 'A'; ch <= 'Z'; ch++) {
            System.out.println(ch);
        }
    }
}